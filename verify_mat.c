#include "fillit.h"

int		verify_mat(char a[5][6])
{
	int i;
	int j;
	int n;
	int q;

	j = 0;
	n = 0;
	i = 0;
	q = 0;
	while (i < 4)
	{
		while (a[i][j] != '\0')
		{
			if (a[i][j] == '#')
				n++;
			if (a[i][j] == '.')
				q++;
			j++;
		}
		j = 0;
		i++;
	}
	if (n != 4 || q != 12 || (checking_mat(a) == 0))
		return (0);
	return (1);
}
