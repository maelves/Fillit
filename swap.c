#include "fillit.h"

void	swap_row(char a[5][6])
{
	int i;
	int j;

	i = 0;
	j = 0;
	while (a[0][0] == '.' && a[0][1] == '.' && a[0][2] == '.' && a[0][3] == '.')
	{
		while (j < 4)
		{
			while (i < 4)
			{
				a[i][j] = a[i + 1][j];
				i++;
			}
			a[--i][j] = '.';
			i = 0;
			j++;
		}
		j = 0;
	}
}

void	swap_coll(char a[5][6])
{
	int i;
	int j;

	i = 0;
	j = 0;
	while (a[0][0] == '.' && a[1][0] == '.' &&
			a[2][0] == '.' && a[3][0] == '.')
	{
		while (i < 4)
		{
			while (a[i][j + 1] != '\0')
			{
				a[i][j] = a[i][j + 1];
				j++;
			}
			a[i][j] = '.';
			j = 0;
			i++;
		}
		i = 0;
	}
}
